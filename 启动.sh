#!/bin/bash

# 邮件处理系统启动器
# 功能：统一启动整个邮件处理系统的所有组件

echo "============================================================"
echo "📧 邮件处理系统启动器"
echo "============================================================"
echo "🕐 启动时间: $(date '+%Y-%m-%d %H:%M:%S')"
echo "📁 工作目录: $(pwd)"
echo "============================================================"

echo ""
echo "🚀 正在启动邮件处理系统..."
echo ""

# 检查Python是否安装
if ! command -v python3 &> /dev/null; then
    echo "❌ 错误: 未找到Python3，请先安装Python3"
    exit 1
fi

# 启动系统
python3 "启动邮件系统.py"

echo ""
echo "系统已退出"
